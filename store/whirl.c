static unsigned
mulX(unsigned v)
{
	v <<= 1;
	if (v > 0xFF)
		v ^= ROOT;
	return v;
}

static unsigned
mul(unsigned a, unsigned b)
{
	unsigned v;
	int i;

	v = 0;
	for (i = 0; i < 8; i ++) {
		if (b & (1 << i))
			v ^= a;
		a = mulX(a);
	}
	return v;
}

static void
apply_gamma(unsigned m[8][8])
{
	int i, j;

	for (i = 0; i < 8; i ++)
		for (j = 0; j < 8; j ++)
			m[i][j] = Sbox[m[i][j]];
}

static void
apply_pi(unsigned m[8][8])
{
	unsigned t[8][8];
	int i, j;

	for (i = 0; i < 8; i ++)
		for (j = 0; j < 8; j ++)
			t[i][j] = m[(8 + i - j) & 7][j];
	memcpy(m, t, sizeof t);
}

static void
apply_theta(unsigned m[8][8])
{
	unsigned t[8][8];
	int i, j, k;

	for (i = 0; i < 8; i ++) {
		for (j = 0; j < 8; j ++) {
			unsigned s;

			s = 0;
			for (k = 0; k < 8; k ++)
				s ^= mul(m[i][k], mds_matrix[k][j]);
			t[i][j] = s;
		}
	}
	memcpy(m, t, sizeof t);
}

static void
apply_comb(unsigned m[8][8])
{
	apply_gamma(m);
	apply_pi(m);
	apply_theta(m);
}

static void
zero(unsigned m[8][8])
{
	int i, j;

	for (i = 0; i < 8; i ++)
		for (j = 0; j < 8; j ++)
			m[i][j] = 0;
}

